public class Operadores {

    public void operadorAritmetico(){
        System.out.println("Explicacion de los operadores aridmeticos");
        int operandoUno = 5;
        operandoUno++;  //Operador uranios +1
        operandoUno++;
        operandoUno++;
        System.out.println("Despues del incremento" +operandoUno);
        operandoUno--; // -1
        operandoUno--; // -1
        System.out.println("Despues del Decremento" +operandoUno);
    } //18:22





















}
